/**
 * Pure function — no filesystem I/O.
 *
 * Truth table:
 *   hasDeclineMarker=true  → 'skip'  (regardless of other inputs)
 *   planningDir non-null   → 'ambient'
 *   hasGitDir=true         → 'init-offer'
 *   (else)                 → 'skip'
 *
 * @param {object} opts
 * @param {string|null} opts.planningDir      - path to .planning/ dir, or null if not found
 * @param {boolean}     opts.hasGitDir        - true if .git/ found in ancestor
 * @param {boolean}     opts.hasDeclineMarker - true if .planning/.devflow-init-declined exists
 * @returns {'ambient'|'init-offer'|'skip'}
 */
function classifySession({ planningDir, hasGitDir, hasDeclineMarker }) {
  if (hasDeclineMarker) return 'skip';
  if (planningDir) return 'ambient';
  if (hasGitDir) return 'init-offer';
  return 'skip';
}

